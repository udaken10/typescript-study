/**
 * 演習 01-2: いろいろなデータ型
 * 
 * 以下の変数に、指定された通りの「型」をつけてください。
 */

// 1. 真偽値（boolean）： true または false が入ります
let isLunchTime: boolean = true;

// 2. 文字列（string）： 学校の名前を自由に入れてください
let schoolName: string = "TypeScript高校";

// 3. 数値（number）： ラッキーナンバーを 777 に設定してください
let luckyNumber: number = 777;

console.log({ isLunchTime, schoolName, luckyNumber });
